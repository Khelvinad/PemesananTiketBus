public class Penumpang {
    private String nama;
    private String NIK;
    private String NoTelp;

    public Penumpang(String nama, String NIK, String NoTelp) {
        this.nama = nama;
        this.NIK = NIK;
        this.NoTelp = NoTelp;
    }

    public String getNama() {return nama;}
    public String getNIK() {return NIK;}
    public String getNoHp() {return NoTelp;}

}
